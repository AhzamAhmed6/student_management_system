        String data[] ={rs.getString("Course"),rs.getString("Student_id"),rs.getString("Assignment_1"),rs.getString("Quiz_1"),rs.getString("Mid"),rs.getString("Assignment_2"),rs.getString("Quiz_2"),rs.getString("Final")};
